	/*
	 * This TaskTableModel adapts the collection of LidaTasks to an
	 * AbstractTableModel
	 */
	private class TaskTableModel extends AbstractTableModel {

		private int columnCnt = 5;

		@Override
		public int getColumnCount() {
			return columnCnt;
		}

		@Override
		public int getRowCount() {
			return taskArray.length;
		}

		@Override
		public String getColumnName(int column) {
			String cName = "";
			switch (column) {
			case 0:
				cName = "Task ID";
				break;
			case 1:
				cName = "Activation";
				break;
			case 2:
				cName = "Status";
				break;
			case 3:
				cName = "description";
				break;
			case 4:
				cName = "Scheduled Tick";
				break;
			default:
				cName = "col" + column;
			}
			return cName;
		}

		@Override
		public Object getValueAt(int rowIndex, int columnIndex) {
			LidaTask task = taskArray[rowIndex];
			Object o = null;
			switch (columnIndex) {
			case 0:
				o = task.getTaskId();
				break;
			case 1:
				o = Math.round(task.getActivation() * 1000.0) / 1000.0;
				break;
			case 2:
				o = task.getStatus();
				break;
			case 3:
				o = task;
				break;
			case 4:
				o = task.getScheduledTick();
				break;
			default:
				o = "";
			}
			return o;
		}

		@Override
		public void setValueAt(Object value, int row, int column) {
		}

		@Override
		public boolean isCellEditable(int row, int column) {
			return false;
		}
	}

